let currentLocale = resolveLocale();

export function getLocale() {
  return currentLocale;
}

export function getLocaleStorageKey() {
  return STORAGE_KEY;
}

export function resolveLocale() {
  const stored = normalizeLocale(readStoredLocale());
  if (stored) return stored;
  return DEFAULT_LOCALE;
}

export function setLocale(locale, options = {}) {
  currentLocale = normalizeLocale(locale) || DEFAULT_LOCALE;
  if (options.persist !== false) writeStoredLocale(currentLocale);
  applyDocumentLocale();
  return currentLocale;
}

export function applyDocumentLocale() {
  document.documentElement.lang = currentLocale;
  document.title = t("app.title");
}

export function t(key, fallback = "") {
  return lookup(messages[currentLocale], key) ?? lookup(messages[DEFAULT_LOCALE], key) ?? (fallback || key);
}

export function tf(key, params = {}, fallback = "") {
  return t(key, fallback).replace(/\{(\w+)\}/g, (_, name) => params[name] ?? "");
}

export function optionText(group, value) {
  return t(`${group}.${value}`, value);
}

export function dateLocale() {
  return currentLocale === ZH_LOCALE ? "zh-CN" : "en-US";
}

export function normalizeLocale(value) {
  if (!value) return "";
  const text = String(value).trim().toLowerCase();
  if (text === "zh-cn" || text === "zh_hans" || text.startsWith("zh")) return ZH_LOCALE;
  if (text === "en" || text.startsWith("en-")) return EN_LOCALE;
  return "";
}

function lookup(source, key) {
  return key.split(".").reduce((value, part) => value?.[part], source);
}

function readStoredLocale() {
  try {
    return window.localStorage.getItem(STORAGE_KEY) || "";
  } catch {
    return "";
  }
}

function writeStoredLocale(locale) {
  try {
    window.localStorage.setItem(STORAGE_KEY, locale);
  } catch {
    // localStorage can be unavailable in hardened browsers; language switching still works for the session.
  }
}
